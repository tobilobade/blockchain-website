// Function to complain and receive tokens
async function complainAndReceiveTokens() {
    try {
        // Ensuring MetaMask is connected and contract is initialized
        await connectMetamaskAndInitializeContract();

        // Getting user address
        const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
        const userAddress = accounts[0];

        // Calling the complain function to receive tokens
        await window.contract.methods.complain().send({ from: userAddress });

        alert('You have received 1 token for your complaint. Thank you!');
        
    } catch (error) {
        console.error('Error complaining and receiving tokens:', error);
        alert('Error: Could not complain and receive tokens. Please try again later.');
    }
}

// Function to send email of ticket raised acknowledgement
async function sendMail() {
    try {
        // Getting the email and message value from the form fields
        const email = document.getElementById('email').value;
        const message = document.getElementById('message').value;

        // Creating data object for the header
        const data = { email, message };

        // Sending the email data to the server using fetch
        const response = await fetch('/send-email', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(data)
        });

        if (response.ok) {
            console.log(email);
            alert('ticket raised successfully! Check Your email');
        } else {
            throw new Error('Failed to send email.');
        }
    } catch (error) {
        console.error('Error sending email:', error);
        alert('Error: Could not send email. Please try again later.');
    }
}
